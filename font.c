const uint8_t* font_num(int n) {
	
	return font_table[n+FONT_NUM_OFFSET];
}
 
const uint8_t* font_alph(char c) {
	//checks special cases
	if (c == '#') {
		return font_table[SHARP_OFFSET];
	} 
	if (c == 'b') {
		return font_table[FLAT_OFFSET];
	}
	if (c == '>') {
		return font_table[FONT_NUM_OFFSET+10];
	}
	if (c == '<') {
		return font_table[FONT_NUM_OFFSET+11];
	}
	if (c == ' ') {
		return font_table[FONT_NUM_OFFSET+12];
	}

	//Ensures A is at location 0
	return font_table[c-'A'];
	
}